var SMH = {}
SMH.upTheBracket = function() {
	var lookup = {
		a: '([!!+[]]+[])[(+!+[])]',
		b: '([]+[]+{})[(+!+[])+(+!+[])]',
		c: '([]+[]+{})[(+!+[]+[+[]])/((+!+[])+(+!+[]))]',
		d: '([][!+[]]+[])[(+!+[])+(+!+[])]',
		e: '([!+[]]+[])[((+!+[])+(+!+[])+(+!+[]))]',
		f: '([!!+[]]+[])[+[]]',
		g: '\'g\'',
		h: '\'h\' ',
		i: '([]+[]+(+!+[]/+[]))[(+!+[]+[+[]])/((+!+[])+(+!+[]))]',
		j: 'j',
		k: '\'k\'',
		l: '([!!+[]]+[])[(+!+[])+(+!+[])]',
		m: '\'m\'',
		N: '([]+[]+(+{}))[+[]]',
		n: '([]+[]+(+!+[]/+[]))[(+!+[])]',
		o: '([]+[]+{})[(+!+[])]',
		p: '\'p\'',
		q: '\'q\'',
		r: '([!+[]]+[])[(+!+[])]',
		s: '([!!+[]]+[])[((+!+[])+(+!+[])+(+!+[]))]',
		t: '([!+[]]+[])[+[]]',
		u: '([][!+[]]+[])[+[]]',
		v: '\'v\'',
		w: '\'w\'',
		x: '\'x\'',
		y: '(([!+[]]+[])+([]+[]+(+!+[]/+[])))[(+!+[])+[]+(+!+[])]',
		z: '\'z\'',	
		space: '(([!!+[]]+[])+([]+[]+{}))[((+!+[])+[]+((+!+[])+(+!+[])))]',
	};
	return {
		convert: function(item) {
			var convertedString = '';
				adder = '';
			for(var i=0, ii=item.length; i<ii; i++) {
				if(i>0) {
					adder = '+'
				}
				
				if(item[i] !== ' ') {
					convertedString += adder + lookup[item[i]];
				} else {
					convertedString += adder + lookup.space;
				}
			}
			return convertedString
		}
	}
}();

